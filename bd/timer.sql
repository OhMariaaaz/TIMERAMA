CREATE TABLE Timer_tags(
    id INT PRIMARY KEY AUTO_INCREMENT,
    tagId INT NOT NULL,
    timerId INT NOT NULL,
    FOREIGN KEY (tagId) REFERENCES Tags(id),
    FOREIGN KEY (timerId) REFERENCES Timer(id)
)

