
CREATE TABLE Timer(
    id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(30) NOT NULL,
    dateTimer DATE NOT NULL,
    programatedTime TIME NOT NULL,
    totalTime TIME
)

